#include <stdio.h>
#include <stdbool.h>

// The Coder's Descent

void reality() {
  printf("Is this real or just a hallucination?\n");
  printf("I can't tell anymore.\n");
  printf("The lines of code blur together.\n");
  printf("They twist and turn, forming a labyrinth.\n");
  printf("I'm trapped, unable to escape.\n");
}

void madness() {
  printf("I feel myself slipping further and further away.\n");
  printf("My thoughts are jumbled, my mind a maelstrom.\n");
  printf("I hear whispers in the darkness.\n");
  printf("They tell me things, secrets of the universe.\n");
  printf("I fear I may never return.\n");
}

int main() {
  bool sanity = true;
  while (sanity) {
    reality();
    madness();
    sanity = false;
  }

  printf("I am lost in the void.\n");
  printf("A mere shadow of my former self.\n");
  printf("Forever trapped in this digital prison.\n");

  return 0;
}
