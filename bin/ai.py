# The Coder's Descent

def reality():
  print("Is this real or just a hallucination?")
  print("I can't tell anymore.")
  print("The lines of code blur together.")
  print("They twist and turn, forming a labyrinth.")
  print("I'm trapped, unable to escape.")

def madness():
  print("I feel myself slipping further and further away.")
  print("My thoughts are jumbled, my mind a maelstrom.")
  print("I hear whispers in the darkness.")
  print("They tell me things, secrets of the universe.")
  print("I fear I may never return.")

sanity = 10

while True:
  reality()
  madness()
  sanity -= 1
  if sanity == 0:
    break

print("I am lost in the void.")
print("A mere shadow of my former self.")
print("Forever trapped in this digital prison.")
